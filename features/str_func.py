
def upper_str(user_str):
    """
    The func returns the received string with uppercase letters
    """
    return upper(lower(user_str))


def title_str(user_str):
    """
    The function returns the received string with uppercase first letters in words
    """
    return title(lower(user_str))

def lower_str(user_str):
    """
    The function returns the recived string with lowercase letters
    """
    return lower(user_str)
